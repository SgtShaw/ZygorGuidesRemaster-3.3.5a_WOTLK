local ZGV = ZygorGuidesViewer
if not (ZGV and ZGV.ItemScore) then return end

local ItemScore = ZGV.ItemScore

local BIS_CATEGORY_KEYS = {"pre_raid", "t7", "t8", "t9", "t10", "rs", "final"}

local function create_empty_build()
	local build = {}
	for _, key in ipairs(BIS_CATEGORY_KEYS) do
		build[key] = {}
	end
	return build
end

local function copy_slot_lists(intoCategory, fromCategory)
	if not intoCategory or not fromCategory then return end
	for slot, itemIDs in pairs(fromCategory) do
		local copied = {}
		for i, itemID in ipairs(itemIDs) do
			copied[i] = itemID
		end
		intoCategory[slot] = copied
	end
end

local function overlay_curated_data(base, curated)
	for classToken, classData in pairs(curated or {}) do
		base[classToken] = base[classToken] or {}
		for buildNum, buildData in pairs(classData) do
			base[classToken][buildNum] = base[classToken][buildNum] or create_empty_build()
			for _, category in ipairs(BIS_CATEGORY_KEYS) do
				copy_slot_lists(base[classToken][buildNum][category], buildData[category])
			end
		end
	end
end

local BISData = {}
for classToken, classRules in pairs(ItemScore.rules or {}) do
	BISData[classToken] = BISData[classToken] or {}
	for buildNum in pairs(classRules) do
		BISData[classToken][buildNum] = create_empty_build()
	end
end

